
# Dictionary is key value pair Example: {}


tel = {'jack': 4098, 'sape': 4139}
tel['guido'] = 4127
print(tel)
print(tel['jack'])

# Using Dictionary constructor
dictionary = dict([('priya', 123), ('Anandha', 456)])
print(dictionary)

# Using keyword arguments
dictOutput = dict(priya=456, Raelyn=123)
print(dictOutput)
print(dictOutput['priya'])
print('Raelyn key\'s value', dictOutput['Raelyn'])

